export const createToken = (
  symbol: string,
  name: string,
  balance: number = 0,
  price: number = 0,
  marketCap?: number,
  change24h?: number,
  decimals: number = 18
): Token => {
  const upperSymbol = symbol.toUpperCase();

  return {
    symbol: upperSymbol,
    name,
    iconUrl: getTokenIconUrl(upperSymbol),
    decimals,
    price,
    balance,
    marketCap,
    change24h,
  };
};

export const createTokens = (): Token[] => {
  return TOKENS.map(
    ({ symbol, name, balance, price, marketCap, change24h, decimals }) =>
      createToken(symbol, name, balance, price, marketCap, change24h, decimals)
  );
};
